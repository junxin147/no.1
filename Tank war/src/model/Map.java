	/**
	 * 
	 * * @Title: backupMap @Description: TODO(备用地图) @param @param level
	 * 设定文件 @return void 返回类型 @throws
	 */
	public void backupMap(int level) {
		for (int i = 0; i < maparray[level - 1].length; i++) {
			for (int j = 0; j < maparray[level - 1][i].length; j++) {
				mapcopy[i][j] = maparray[level - 1][i][j];
			}
		}
	}

